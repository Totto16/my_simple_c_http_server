function uniqueArr<T>(arr: T[]): T[] {
    return [...new Set<T>(arr)]
}

function arrayIsEq<T>(arr1: T[], arr2: T[]): boolean {
    if (arr1.length != arr2.length) {
        return false;
    }

    for (let i = 0; i < arr1.length; ++i) {
        if (arr1.at(i) != arr2.at(i)) {
            return false;
        }
    }

    return true;
}

