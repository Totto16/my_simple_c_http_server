function getValue(code: HuffmanCode): EndValue {
    if (code.type === "normal") {
        return code.value
    }

    // eslint-disable-next-line @typescript-eslint/no-unnecessary-condition
    if (code.special === "EOS") {
        return "EOS"
    }

    throw new Error("Error")
}

function checkValidRootTree1(root: HuffmanNode): void {

    if (root.id === null) {
        throw new Error("NULL id on node")
    }

    if (root.type === "end") {
        if (root.value === null) {
            throw new Error("NULL end node")
        }
        return;
    }

    const node = root.node

    checkValidRootTree1(node.bitZero)
    checkValidRootTree1(node.bitOne)

}

